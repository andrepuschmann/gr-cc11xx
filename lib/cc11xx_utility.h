class cc11xx_utility
{
public:
    /**
     * \brief Returns a single byte from an array of zeros and ones.
     *
     * \param in - The buffer.
     * \param offset - The offset within the buffer.
     * \return uint8_t - The byte.
     */
    static uint8_t read_byte_from_input(const uint8_t* in, int offset = 0)
    {
        uint8_t byte = 0;
        for (int i = 0; i < 8; i++) {
            if (in[offset + i] == 1)
                byte = (byte << 1) | 1;
            else
                byte = byte << 1;
        }
        //if (VERBOSE)
            //std::cout << boost::format("New byte: 0x%x\n") % (static_cast<int>(byte));
        return byte;
    }


    // http://www.ti.com/lit/an/swra322/swra322.pdf
    static int xor_pn9(uint8_t *buf, size_t len)
    {
        if (len > sizeof(pn9_table))
            return -1;

        for (int i = 0; i < len; ++i)
            buf[i] ^= pn9_table[i];

        return 0;
    }

    // Code taken from CRC app note: http://www.ti.com/lit/an/swra111d/swra111d.pdf
    static uint16_t calc_crc_step(uint8_t crcData, uint16_t crcReg)
    {
        const uint16_t crc_poly = 0x8005;
        for (size_t i = 0; i < 8; i++) {
            if (((crcReg & 0x8000) >> 8) ^ (crcData & 0x80))
                crcReg = (crcReg << 1) ^ crc_poly;
            else
                crcReg = (crcReg << 1);
            crcData <<= 1;
        }
        return crcReg;
    }

    static uint16_t calc_crc(uint8_t *buf, size_t len)
    {
        uint16_t checksum = 0xFFFF;
        for (size_t i = 0; i < len; i++)
            checksum = calc_crc_step(buf[i], checksum);
        return checksum;
    }

};

